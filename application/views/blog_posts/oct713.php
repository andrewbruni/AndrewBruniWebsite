            This past weekend we took a trip to Biarritz, France and San Sebastián, Spain. 
            	<br /> 
                <br /> 
            Things I learned in France:
            	<br /> 
            	&nbsp; &nbsp; &nbsp;a) I do not speak any French. I can't remember at any point in my life before last weekend, a time when I genuinely could not understand someone who was talking to me. It was a weird feeling not having any clue what was being said. Even when around my friend Chris, who speaks Afrikaans at home, I can pick up hints of what his family is saying. Not in France. 
                <br />  
                &nbsp; &nbsp; &nbsp; b) France is expensive. 20 Euro for a small glass of sangria, a small pizza, and a small salad. 3 Euro for a bottle of Coke and 2 Euro for an even smaller bottle of soda. 
                <br />  &nbsp; &nbsp; &nbsp; c) It is weird being able to cross borders without being stopped for your papers, the EU is cool. 
                
                <br /> 
                <br />
            The town of Biarritz was very nice however; the hotel we stayed at was right next to the water and the view of the ocean was great. 
            	
                <br />
                <br />
            San Sebastián was interesting as well; it seemed very laid back and more of a tourists city than others I've visited. The beaches in San Sebastián were the best I've seen while in Spain. Walking around the city, much of it was based on French architecture, there was even a bridge that was a replica of one in Paris. 
             	<br /> 
                <br />
            Overall, it was a fun weekend and I'm glad to have had the chance to visit France again. 
                 
                
            
