        	<h1 id=blogPostHead><a href="../blog">October 21, 2013</a></h1>
        	<p id=blogPostP>Aside from the on and off rain storms all weekend, our time in Dublin was great. The city itself is amazing and the people were very nice as well.
        	
            <br />
            <br />
            
            My roommate Skyler and I met up at the Dublin airport and started our weekend Thursday night by checking in to the Isaac Hostel near the center of the city. After dropping our bags off, we headed out to find dinner. We ended up walking the complete wrong direction of any pubs or restaurants and returned to the hostel to admit defeat and ask for some suggestions. They recommended a pizza place where we had a small pizza, fries and a drink for only €5 (literally the cheapest thing we bought the entire trip...). After that we walked until we found a pub that had some live music and had ourselves a pint of Guinness. 
            <br />
            <br />
            
        	Later Thursday night we headed over to Temple Bar and stopped at a pub called The Temple Bar for a pint of cider before heading back to the hostel to get some sleep.
            
            <br />
            <br />
            
        	Friday morning, we woke up early, grabbed breakfast and trekked on over to the Guinness Storehouse for a self guided tour, lunch and a pint at the Guinness Gravity Bar. The tour itself showed the history of the beer and the process of making it; it was very well put together. 
            
            <br />
            <br />
            
        	For lunch, we split a minced meat burger that was marinated in Guinness and a plate of mussels that was also made in a sauce of Guinness. Finally, for good measure, we tried some bread that was made with Guinness. Everything we ate was delicious (and made with Guinness). 
            
            <br />
            <br />
            
        	After lunch, we wandered up to the Gravity Bar; it was a 360 degree glass walled bar where you could see the entire city. We ordered our beers and sat around checking out the sites. Once finished, we headed over to Kilmainham Gaol, the oldest jail in Dublin, for a tour. 
            
            <br />
            <br />
			
            The tour of the jail was better than I expected for €2; the building was well kept and pretty creepy inside knowing how many people died there.
            
            <br />
            <br />
            
        	On Friday night, we signed up for a pub crawl through Temple Bar with some other friends from school and had a crazy night. The crawl led us through four bars and a night club to finish off the night. With the cost of the pub crawl ticket, we got a half pint of beer,  a free shot at each of the  bars, drink discounts and no cover charges at any of the places we went.. We drank, made some new friends who happened to be from the US, and had fun all night. 
            
            <br />
            <br />
            
        	Saturday morning was spent sleeping after walking into our hostel around 4:30 that morning. We spent the day taking it easy, grabbed lunch and walked around the main shopping strip before finding some place for dinner. 
            
            <br />
            <br />
            
        	For dinner we found a pub called The Ginger Man, which was decorated with everything you might relate to Halloween and fall. I ate some fish and chips and Skyler ordered a shepard's pie made with more Guinness. After finishing our food and cider, we headed out to pub hop for the rest of the night. We stopped at two more pubs and again for some late night Indian food before heading to bed for our 5 a.m. airport trip the next morning. 
            
            <br />
            <br />
            
        	Overall, Dublin was a great trip and I plan on going back in the future, I loved every minute we spent there. </p>
